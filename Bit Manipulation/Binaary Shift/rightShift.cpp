#include <bits/stdc++.h>
using namespace std;

int main()
{
    /*
        Right Shift Operator = >>
        It shifts all bits to the right by specified number of positions.
        Each right shift operation effectively divides the number by 2.

        Example:
        8 >> 1 = 4  (8 / 2^1 = 4)
        8 >> 2 = 2  (8 / 2^2 = 2)
    */

    cout << (8 >> 1) << endl; // Output: 4
    cout << (8 >> 2) << endl; // Output: 2
}