#include<bits/stdc++.h>
using namespace std;

int main() {
    /*
        Left Shift Operator = <<
        It shifts all bits to the left by specified number of positions.
        Each left shift operation effectively multiplies the number by 2.

        Example:
        3 << 1 = 6  (3 * 2^1 = 6)
        3 << 2 = 12 (3 * 2^2 = 12)
    */

    cout << (3 << 1) << endl; // Output: 6
    cout << (3 << 2) << endl; // Output: 12
}